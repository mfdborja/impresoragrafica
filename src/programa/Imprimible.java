    public int print(Graphics graphics, PageFormat pageFormat, int pageIndex) throws PrinterException {

        if (pageIndex > 0) {
            //Solo tenemos una página.
            //Al devolver esto indicamos que no hay más páginas
            //Print se estará ejecutando hasta que le digamos esto
            return NO_SUCH_PAGE;
        }

        Graphics2D imprimible = (Graphics2D) graphics;
        //Transforma para que los pixeles se correspondan con la posición en la página en papel.
        imprimible.translate(pageFormat.getImageableX(), pageFormat.getImageableY());

        //Esto imprime
        this.printAll(imprimible);

        //Y con esto decimos que todo está bien.
        return PAGE_EXISTS;

    }
